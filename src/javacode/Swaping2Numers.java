package javacode;

public class Swaping2Numers {
public static void main(String[] args) {
	
	
	int a=10, b=20;
	System.out.println("before swapping value are "+a+"   "+b);
	// logic third variable
	int t=a;
	a=b;
	b=t;
	
	System.out.println("after swapping value are "+a+"   " +b);

	
	
	
	




}





}
